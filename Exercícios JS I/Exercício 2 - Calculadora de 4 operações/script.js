let primeiroNumero = window.parseFloat(prompt("Digite o primeiro número: "))
let segundoNumero = window.parseFloat(prompt("Digite o segundo número: "))

let soma = primeiroNumero + segundoNumero
let subtracao = primeiroNumero - segundoNumero
let multiplicacao = primeiroNumero * segundoNumero
let divisao = primeiroNumero / segundoNumero

document.write("A soma dos dois números é " + soma)
document.write("<br>A subtração dos dois números é " + subtracao)
document.write("<br>A multiplicação dos dois números é " + multiplicacao)
document.write("<br>A divisão dos dois números é " + divisao)

